NULL as data_istruttoria, -- DATE_FORMAT( i.data_validazione_atc, '%d/%m/%Y') as data_istruttoria, -- errata
DATE_FORMAT( p.data_conclusione_progetto, '%d/%m/%Y') as data_conclusione_progetto, -- errata
DATE_FORMAT(i.data_termine_progetto, '%d/%m/%Y') as data_fine_rendicontazione,
