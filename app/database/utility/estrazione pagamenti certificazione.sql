COALESCE(CONCAT(protocollo.registro_pg, '/', protocollo.anno_pg, '/', protocollo.num_pg), r.id) as protocollo,
REPLACE(REPLACE(r.titolo, '\n', ' '), '\r', '') as titolo_progetto,
modalita.descrizione as tipo_pagamento,
p.id as id_pagamento,
p.importo_rendicontato,
p.importo_rendicontato_ammesso,
mandato.numero_mandato,
