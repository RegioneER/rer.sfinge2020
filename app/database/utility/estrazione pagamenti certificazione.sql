cp.importo,
c.anno as anno_certificazione,
c.numero as numero_certificazione,
rp.codice as codice_pagamento

from pagamenti as p

join attuazione_controllo_richieste as atc
on atc.id = p.attuazione_controllo_richiesta_id

join richieste as r
on r.id = atc.richiesta_id

join procedure_operative as po
on po.id = r.procedura_id

join richieste_protocollo as protocollo
on protocollo.richiesta_id = r.id 
and protocollo.data_cancellazione is null
and protocollo.tipo =  'FINANZIAMENTO'

join modalita_pagamento as modalita
on modalita.id = p.modalita_pagamento_id

join mandati_pagamenti as mandato
on mandato.id = p.mandato_pagamento_id



join istruttorie_richieste as i
on i.richiesta_id = r.id
and i.data_cancellazione is null

left join certificazioni_pagamenti as cp
on cp.pagamento_id = p.id

left join certificazioni as c
on c.id = cp.certificazione_id

left join richieste_pagamenti as rp
on rp.pagamento_id = p.id
and rp.data_cancellazione is null


